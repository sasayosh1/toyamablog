const { createClient } = require('@sanity/client');

const client = createClient({
  projectId: 'aoxze287',
  dataset: 'production',
  apiVersion: '2024-01-01',
  useCdn: false,
  token: process.env.SANITY_API_TOKEN
});

async function expandMediumArticle(article) {
  try {
    const post = await client.fetch(`*[_type == "post" && slug.current == "${article.slug}"][0] { _id, title, body }`);
    if (!post) throw new Error(`記事が見つかりません: ${article.slug}`);

    // 現在の文字数カウント
    let currentChars = 0;
    post.body.forEach(block => {
      if (block._type === 'block' && block.children) {
        const text = block.children.map(child => child.text || '').join('');
        currentChars += text.length;
      }
    });

    // 既存コンテンツを保持
    const existingContent = [...post.body];
    
    // 新しい高品質コンテンツを追加（1800-2200文字に拡張、3つのH2セクション）
    const expandedBody = [
      // 導入文
      {
        _type: 'block',
        _key: `intro-${Date.now()}`,
        style: 'normal',
        children: [{
          _type: 'span',
          _key: `span-intro-${Date.now()}`,
          text: article.content[0].intro,
          marks: []
        }],
        markDefs: []
      },
      // 既存コンテンツを保持
      ...existingContent,
      // H2セクション1
      {
        _type: 'block',
        _key: `h2-1-${Date.now()}`,
        style: 'h2',
        children: [{
          _type: 'span',
          _key: `span-h2-1-${Date.now()}`,
          text: article.content[1].h2,
          marks: []
        }],
        markDefs: []
      }
    ];

    // H3セクション1-1, 1-2
    article.content[1].h3sections.forEach((h3section, index) => {
      expandedBody.push({
        _type: 'block',
        _key: `h3-1-${index}-${Date.now()}`,
        style: 'h3',
        children: [{
          _type: 'span',
          _key: `span-h3-1-${index}-${Date.now()}`,
          text: h3section.h3,
          marks: []
        }],
        markDefs: []
      });
      expandedBody.push({
        _type: 'block',
        _key: `h3content-1-${index}-${Date.now()}`,
        style: 'normal',
        children: [{
          _type: 'span',
          _key: `span-h3content-1-${index}-${Date.now()}`,
          text: h3section.text,
          marks: []
        }],
        markDefs: []
      });
    });

    // H2セクション2
    expandedBody.push({
      _type: 'block',
      _key: `h2-2-${Date.now()}`,
      style: 'h2',
      children: [{
        _type: 'span',
        _key: `span-h2-2-${Date.now()}`,
        text: article.content[2].h2,
        marks: []
      }],
      markDefs: []
    });

    // H3セクション2-1, 2-2
    article.content[2].h3sections.forEach((h3section, index) => {
      expandedBody.push({
        _type: 'block',
        _key: `h3-2-${index}-${Date.now()}`,
        style: 'h3',
        children: [{
          _type: 'span',
          _key: `span-h3-2-${index}-${Date.now()}`,
          text: h3section.h3,
          marks: []
        }],
        markDefs: []
      });
      expandedBody.push({
        _type: 'block',
        _key: `h3content-2-${index}-${Date.now()}`,
        style: 'normal',
        children: [{
          _type: 'span',
          _key: `span-h3content-2-${index}-${Date.now()}`,
          text: h3section.text,
          marks: []
        }],
        markDefs: []
      });
    });

    // H2セクション3
    expandedBody.push({
      _type: 'block',
      _key: `h2-3-${Date.now()}`,
      style: 'h2',
      children: [{
        _type: 'span',
        _key: `span-h2-3-${Date.now()}`,
        text: article.content[3].h2,
        marks: []
      }],
      markDefs: []
    });

    // H3セクション3-1, 3-2
    article.content[3].h3sections.forEach((h3section, index) => {
      expandedBody.push({
        _type: 'block',
        _key: `h3-3-${index}-${Date.now()}`,
        style: 'h3',
        children: [{
          _type: 'span',
          _key: `span-h3-3-${index}-${Date.now()}`,
          text: h3section.h3,
          marks: []
        }],
        markDefs: []
      });
      expandedBody.push({
        _type: 'block',
        _key: `h3content-3-${index}-${Date.now()}`,
        style: 'normal',
        children: [{
          _type: 'span',
          _key: `span-h3content-3-${index}-${Date.now()}`,
          text: h3section.text,
          marks: []
        }],
        markDefs: []
      });
    });

    // まとめ
    expandedBody.push({
      _type: 'block',
      _key: `conclusion-${Date.now()}`,
      style: 'normal',
      children: [{
        _type: 'span',
        _key: `span-conclusion-${Date.now()}`,
        text: article.content[4].conclusion,
        marks: []
      }],
      markDefs: []
    });

    await client.patch(post._id).set({ body: expandedBody }).commit();
    
    // 新文字数カウント
    let newChars = 0;
    expandedBody.forEach(block => {
      if (block._type === 'block' && block.children) {
        const text = block.children.map(child => child.text || '').join('');
        newChars += text.length;
      }
    });

    return { 
      success: true, 
      title: article.title, 
      id: article.id,
      charsBefore: currentChars, 
      charsAfter: newChars,
      expansion: newChars - currentChars
    };

  } catch (error) {
    return { 
      success: false, 
      title: article.title, 
      id: article.id,
      error: error.message 
    };
  }
}

